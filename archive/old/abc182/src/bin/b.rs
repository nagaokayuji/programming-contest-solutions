fn main() {
    input! {//
        n:usize,
        mut a:[i64;n],
    }
    let mut ans = 1;
    let mut best = 0;
    for i in 2..=1000 {
        let mut cnt = 0;
        for &x in a.iter() {
            if x % i == 0 {
                cnt += 1;
            }
        }
        if chmax!(best, cnt) {
            ans = i;
        }
    }
    println!("{}", ans);
}
pub fn gcd(a: i64, b: i64) -> i64 {
    if b == 0 {
        a
    } else {
        gcd(b, a % b)
    }
}
pub fn gcd_list(list: &[i64]) -> i64 {
    list.iter().fold(list[0], |a, &b| gcd(a, b))
}
